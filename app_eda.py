class EDA:
    def __init__(self):
        st.title("\U0001F4CA Population Trends EDA")
        uploaded = st.file_uploader("population_trends.csv ÏóÖÎ°úÎìú", type="csv")
        if not uploaded:
            st.info("CSV ÌååÏùºÏùÑ ÏóÖÎ°úÎìúÌï¥Ï£ºÏÑ∏Ïöî.")
            return

        df = pd.read_csv(uploaded)
        df.replace('-', 0, inplace=True)
        for col in ['Ïù∏Íµ¨', 'Ï∂úÏÉùÏïÑÏàò(Î™Ö)', 'ÏÇ¨ÎßùÏûêÏàò(Î™Ö)']:
            df[col] = pd.to_numeric(df[col], errors='coerce')

        tab1, tab2, tab3, tab4, tab5 = st.tabs([
            "Í∏∞Ï¥à ÌÜµÍ≥Ñ", "Ïó∞ÎèÑÎ≥Ñ Ï∂îÏù¥", "ÏßÄÏó≠Î≥Ñ Î∂ÑÏÑù", "Î≥ÄÌôîÎüâ Î∂ÑÏÑù", "ÏãúÍ∞ÅÌôî"
        ])

        with tab1:
            st.subheader("Í∏∞Ï¥à ÌÜµÍ≥Ñ")
            st.write("Í≤∞Ï∏°Ïπò:", df.isnull().sum())
            st.write("Ï§ëÎ≥µ:", df.duplicated().sum())
            buffer = io.StringIO()
            df.info(buf=buffer)
            st.text(buffer.getvalue())
            st.dataframe(df.describe())

        with tab2:
            st.subheader("Ïó∞ÎèÑÎ≥Ñ Ïù∏Íµ¨ Ï∂îÏù¥")
            nation = df[df['ÏßÄÏó≠'] == 'Ï†ÑÍµ≠']
            fig, ax = plt.subplots()
            ax.plot(nation['Ïó∞ÎèÑ'], nation['Ïù∏Íµ¨'], marker='o')
            ax.set_title("National Population Trend")
            ax.set_xlabel("Year")
            ax.set_ylabel("Population")
            last = nation['Ïó∞ÎèÑ'].max()
            birth_mean = nation.tail(3)['Ï∂úÏÉùÏïÑÏàò(Î™Ö)'].mean()
            death_mean = nation.tail(3)['ÏÇ¨ÎßùÏûêÏàò(Î™Ö)'].mean()
            predict = nation['Ïù∏Íµ¨'].values[-1] + (birth_mean - death_mean)*(2035 - last)
            ax.axhline(predict, color='red', linestyle='--')
            ax.text(2034, predict, f"Predicted: {int(predict):,}")
            st.pyplot(fig)

        with tab3:
            st.subheader("ÏµúÍ∑º 5ÎÖÑ ÏßÄÏó≠Î≥Ñ Ïù∏Íµ¨ Î≥ÄÌôîÎüâ")
            recent = df[df['Ïó∞ÎèÑ'] >= df['Ïó∞ÎèÑ'].max() - 5]
            pivot = recent.pivot(index='ÏßÄÏó≠', columns='Ïó∞ÎèÑ', values='Ïù∏Íµ¨')
            pivot = pivot.drop('Ï†ÑÍµ≠', errors='ignore')
            pivot['change'] = pivot[pivot.columns[-1]] - pivot[pivot.columns[0]]
            pivot['rate'] = (pivot['change'] / pivot[pivot.columns[0]]) * 100
            pivot.sort_values('change', ascending=False, inplace=True)

            fig1, ax1 = plt.subplots()
            sns.barplot(x=pivot['change']/1000, y=pivot.index, ax=ax1)
            ax1.set_title("Population Change (K)")
            st.pyplot(fig1)

            fig2, ax2 = plt.subplots()
            sns.barplot(x=pivot['rate'], y=pivot.index, ax=ax2)
            ax2.set_title("Change Rate (%)")
            st.pyplot(fig2)

        with tab4:
            st.subheader("Ï¶ùÍ∞ê ÏÉÅÏúÑ 100Í±¥")
            df2 = df[df['ÏßÄÏó≠'] != 'Ï†ÑÍµ≠'].copy()
            df2['change'] = df2.groupby('ÏßÄÏó≠')['Ïù∏Íµ¨'].diff()
            top100 = df2.sort_values('change', ascending=False).head(100)

            def highlight(val):
                color = 'background-color: lightblue' if val > 0 else 'background-color: salmon'
                return color

            st.dataframe(top100.style.applymap(highlight, subset=['change']).format({
                'change': '{:,.0f}'
            }))

        with tab5:
            st.subheader("ÎàÑÏ†Å ÏòÅÏó≠ ÏãúÍ∞ÅÌôî")
            pivot = df.pivot(index='Ïó∞ÎèÑ', columns='ÏßÄÏó≠', values='Ïù∏Íµ¨')
            pivot = pivot.drop('Ï†ÑÍµ≠', axis=1, errors='ignore')

            fig, ax = plt.subplots(figsize=(10, 6))
            pivot.fillna(0).plot.area(ax=ax)
            ax.set_title("Population by Region")
            ax.set_xlabel("Year")
            ax.set_ylabel("Population")
            st.pyplot(fig)

